plt.title('Agent Learning Curve: Steps taken per Episode', fontsize=16)
plt.xlabel('Episode')
plt.ylabel('Number of Steps')
plt.grid(True, alpha=0.3)
plt.legend()
plt.show()

print("Evaluation complete. A decreasing trend in the graph proves the agent is learning effectively.")